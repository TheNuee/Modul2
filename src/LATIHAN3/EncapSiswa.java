package LATIHAN3;

public class EncapSiswa {

    private String name;
    private String adress;
    private int absen;

    public int getAbsen() {
        return absen;
    }

    public String getName() {
        return name;
    }

    public String getAddress() {
        return adress;
    }

    public void setAbsen(int newAbsen) {
        absen = newAbsen;
    }

    public void setName(String newName) {
        name = newName;
    }

    public void setAdress(String newAdress) {
        adress = newAdress;
    }
}
